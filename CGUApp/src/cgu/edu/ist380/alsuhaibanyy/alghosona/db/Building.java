package cgu.edu.ist380.alsuhaibanyy.alghosona.db;

public class Building {
	
	private int mBuildingID;
	private String mBuildingName;
	private String mBuildingAddress;
	
	public int getmBuildingID() {
		return mBuildingID;
	}
	public void setmBuildingID(int mBuildingID) {
		this.mBuildingID = mBuildingID;
	}
	public String getmBuildingName() {
		return mBuildingName;
	}
	public void setmBuildingName(String mBuildingName) {
		this.mBuildingName = mBuildingName;
	}
	public String getmBuildingAddress() {
		return mBuildingAddress;
	}
	public void setmBuildingAddress(String mBuildingAddress) {
		this.mBuildingAddress = mBuildingAddress;
	}
	

	
}
